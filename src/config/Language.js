export const languageSetting = (mode) => {
  const language = tokens(mode);
  return {
    user: {
      admin: language.user["admin"],
      manager: language.user["manager"],
      user: language.user["user"],
    },
    sidebar: {
      overview: language.sidebar["overview"],
      app: language.sidebar["app"],
      commerce: language.sidebar["commerce"],
      files: language.sidebar["files"],
      calendar: language.sidebar["calendar"],
      managment: language.sidebar["managment"],
      list: language.sidebar["list"],
      create: language.sidebar["create"],
    },
    setting: {
      setting: language.setting["setting"],
      mode: language.setting["mode"],
      contrast: language.setting["contrast"],
      direction: language.setting["direction"],
      layout: language.setting["layout"],
      stretch: language.setting["stretch"],
      presets: language.setting["presets"],
      api: language.setting["api"],
      local: language.setting["local"],
      global: language.setting["global"],
      fullscreen: language.setting["fullscreen"],
    },
    loginPage: {
      left: "Hi, Welcome back",
    },
  };
};
export const useMode = () => {
  const language = useSelector((state) => state.languageReducer.language);
  const Language = useMemo(() => languageSetting(language), [language]);

  return Language;
};
export default useMode;

